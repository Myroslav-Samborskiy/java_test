class SwitchBoard {
    public static void main(String[] args) {
        int iSwitch = 4;
        switch (iSwitch) {
            case 0:
                System.out.println("Zero");
                break;
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            case 4:
                System.out.println("Four");
                break;

                default:
                    System.out.println("Not in the list");
                    break;

        }
    }
}
